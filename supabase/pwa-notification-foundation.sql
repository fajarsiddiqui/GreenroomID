-- Mengubah setiap audit log baru menjadi inbox notification untuk seluruh admin aktif.
create or replace function public.create_admin_notifications_from_audit_log()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  payload jsonb := to_jsonb(new);
  action_value text := coalesce(nullif(payload ->> 'action', ''), 'activity');
  description_value text := coalesce(nullif(payload ->> 'description', ''), 'Ada aktivitas baru di GreenroomID.');
  request_value text := nullif(payload ->> 'request_id', '');
  log_value text := nullif(payload ->> 'id', '');
  action_search text;
  priority_value text := 'info';
  title_value text;
  route_value text := '/admin/audit-logs';
  admin_record record;
begin
  action_search := lower(action_value || ' ' || description_value);

  if action_search ~ '(security|mencurigakan|gagal|failed|error|hapus permanen|delete permanent)' then
    priority_value := 'critical';
  elsif action_search ~ '(payment|pembayaran|request baru|created request|verifikasi|verified|revisi|deadline)' then
    priority_value := 'important';
  elsif action_search ~ '(form|status|upload|file|learning|pembelajaran|donasi|message|pesan)' then
    priority_value := 'operational';
  end if;

  title_value := initcap(replace(replace(action_value, '_', ' '), '-', ' '));
  if title_value = '' then title_value := 'Aktivitas baru'; end if;

  if request_value is not null then
    route_value := '/admin/requests';
  elsif action_search ~ '(payment|pembayaran)' then
    route_value := '/admin/stats';
  elsif action_search ~ '(form)' then
    route_value := '/admin/forms';
  elsif action_search ~ '(learning|pembelajaran)' then
    route_value := '/admin/ruang-belajar/review';
  elsif action_search ~ '(donasi|donation)' then
    route_value := '/admin/donations';
  end if;

  for admin_record in
    select id from public.user_profiles
    where role = 'admin' and is_active = true
  loop
    insert into public.notifications (
      recipient_user_id,
      activity_log_id,
      type,
      priority,
      title,
      body,
      target_route,
      metadata
    ) values (
      admin_record.id,
      log_value,
      'audit_log',
      priority_value,
      title_value,
      description_value,
      route_value,
      payload
    )
    on conflict do nothing;
  end loop;

  return new;
end;
$$;

drop trigger if exists audit_logs_create_admin_notifications on public.audit_logs;
create trigger audit_logs_create_admin_notifications
after insert on public.audit_logs
for each row execute function public.create_admin_notifications_from_audit_log();

