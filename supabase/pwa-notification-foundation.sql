-- RPC pengujian end-to-end dari browser admin.
create or replace function public.create_test_push_notification()
returns uuid
language plpgsql
security definer
set search_path = public
as $$
declare
  created_id uuid;
begin
  if auth.uid() is null or not public.is_notification_admin() then
    raise exception 'Only admin can create test notification';
  end if;

  insert into public.notifications (
    recipient_user_id,
    type,
    priority,
    title,
    body,
    target_route,
    metadata
  ) values (
    auth.uid(),
    'system_test',
    'important',
    'Tes notifikasi GreenroomID',
    'Jika pesan ini muncul di HP, PWA, Supabase, webhook, dan Firebase sudah terhubung.',
    '/admin',
    jsonb_build_object('source', 'manual_test', 'created_by', auth.uid())
  ) returning id into created_id;

  return created_id;
end;
$$;

grant execute on function public.create_test_push_notification() to authenticated;
