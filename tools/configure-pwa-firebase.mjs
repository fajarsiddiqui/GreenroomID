import { readFile, writeFile } from 'node:fs/promises'
import { createInterface } from 'node:readline/promises'
import { stdin as input, stdout as output } from 'node:process'
import path from 'node:path'

const projectRoot = path.resolve(process.argv[2] || '.')
const targetPath = path.join(projectRoot, 'public', 'firebase-config.js')

function extractExisting(source = '') {
  const value = (key) => {
    const match = source.match(new RegExp(`${key}\\s*:\\s*['\"]([^'\"]*)['\"]`))
    return match?.[1] || ''
  }
  return {
    apiKey: value('apiKey'),
    authDomain: value('authDomain'),
    projectId: value('projectId'),
    storageBucket: value('storageBucket'),
    messagingSenderId: value('messagingSenderId'),
    appId: value('appId'),
    measurementId: value('measurementId'),
    vapidKey: value('vapidKey')
  }
}

let existing = {}
try {
  existing = extractExisting(await readFile(targetPath, 'utf8'))
} catch {
  existing = {}
}

const rl = createInterface({ input, output })
const ask = async (label, key, required = true) => {
  const suffix = existing[key] ? ` [Enter = pertahankan ${existing[key]}]` : ''
  while (true) {
    const answer = (await rl.question(`${label}${suffix}: `)).trim()
    const value = answer || existing[key] || ''
    if (value || !required) return value
    console.log('Nilai ini wajib diisi.')
  }
}

console.log('\nGreenroomID · Konfigurasi Firebase Web Push')
console.log('Salin nilai dari Firebase Console > Project settings > Your apps > Web app.\n')

const config = {
  apiKey: await ask('apiKey', 'apiKey'),
  authDomain: await ask('authDomain', 'authDomain'),
  projectId: await ask('projectId', 'projectId'),
  storageBucket: await ask('storageBucket', 'storageBucket', false),
  messagingSenderId: await ask('messagingSenderId', 'messagingSenderId'),
  appId: await ask('appId', 'appId'),
  measurementId: await ask('measurementId (opsional)', 'measurementId', false),
  vapidKey: await ask('VAPID public key', 'vapidKey')
}

rl.close()

const content = `/* Generated by tools/configure-pwa-firebase.mjs */\nself.GREENROOM_FIREBASE_CONFIG = ${JSON.stringify(config, null, 2)}\n`
await writeFile(targetPath, content, 'utf8')
console.log(`\nSelesai: ${targetPath}`)
console.log('Jalankan npm run dev, lalu buka Admin > ikon lonceng > Aktifkan.\n')
